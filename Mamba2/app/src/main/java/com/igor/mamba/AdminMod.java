package com.igor.mamba;

public class AdminMod {

    private String admin_id;
    private String admin_contact;
    public String admin_job;

    public AdminMod() {
    }

    public String getAdmin_id() {
        return admin_id;
    }

    public void setAdmin_id(String admin_id) {
        this.admin_id = admin_id;
    }

    public String getAdmin_contact() {
        return admin_contact;
    }

    public void setAdmin_contact(String admin_contact) {
        this.admin_contact = admin_contact;
    }

    public String getAdmin_job() {
        return admin_job;
    }

    public void setAdmin_job(String admin_job) {
        this.admin_job = admin_job;
    }

}
